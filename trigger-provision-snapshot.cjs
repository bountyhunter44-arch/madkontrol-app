const admin = require('firebase-admin');
const serviceAccount = require('./madkontrollen-firebase-adminsdk.json');

admin.initializeApp({
  credential: admin.credential.cert(serviceAccount)
});

const { generateRiskAnalysisSnapshot } = require('./functions/riskAnalysisLibrary');

async function provisionSnapshot() {
  const companyId = 'company_1777918459234_s1hq3qv7o';
  const locationId = 'location_1777918459234_me2rtaroj';
  const industry = 'restaurant';
  
  console.log('[provisionSnapshot] START', { companyId, locationId, industry });
  
  try {
    // Generate snapshot using backend library
    const snapshot = generateRiskAnalysisSnapshot({
      companyId,
      locationId,
      organizationId: companyId,
      industry,
      companyName: 'Test Restaurant',
      profile: {} // Empty profile for now
    });
    
    console.log('[provisionSnapshot] Generated snapshot:', {
      version: snapshot.version,
      controlPointsCount: snapshot.controlPoints.length,
      firstControlPoint: snapshot.controlPoints[0]?.key,
      firstTitle: snapshot.controlPoints[0]?.title
    });
    
    // Save to Firestore
    const db = admin.firestore();
    
    // Check for existing snapshots
    const existingSnapshotsQuery = await db.collection('haccp_snapshots')
      .where('companyId', '==', companyId)
      .where('locationId', '==', locationId)
      .orderBy('createdAt', 'desc')
      .limit(1)
      .get();
    
    if (!existingSnapshotsQuery.empty) {
      const existingDoc = existingSnapshotsQuery.docs[0];
      const existingData = existingDoc.data();
      
      console.log('[provisionSnapshot] Found existing snapshot:', {
        docId: existingDoc.id,
        version: existingData.version,
        autoGenerated: existingData.autoGenerated,
        manualEdited: existingData.manualEdited
      });
      
      if (existingData.manualEdited || existingData.manualOverride) {
        console.log('[provisionSnapshot] SKIPPED - Manual edits present');
        return {
          ok: true,
          skipped: true,
          reason: 'manual_edits_present',
          snapshotId: existingDoc.id
        };
      }
      
      // Update existing auto-generated snapshot
      await existingDoc.ref.update({
        ...snapshot,
        updatedAt: admin.firestore.FieldValue.serverTimestamp()
      });
      
      console.log('[provisionSnapshot] UPDATED existing snapshot:', existingDoc.id);
      
      return {
        ok: true,
        updated: true,
        snapshotId: existingDoc.id,
        version: snapshot.version,
        controlPointsCount: snapshot.controlPoints.length
      };
    }
    
    // Create new snapshot
    const docRef = await db.collection('haccp_snapshots').add({
      ...snapshot,
      createdAt: admin.firestore.FieldValue.serverTimestamp(),
      updatedAt: admin.firestore.FieldValue.serverTimestamp()
    });
    
    console.log('[provisionSnapshot] CREATED new snapshot:', docRef.id);
    
    return {
      ok: true,
      created: true,
      snapshotId: docRef.id,
      version: snapshot.version,
      controlPointsCount: snapshot.controlPoints.length
    };
    
  } catch (error) {
    console.error('[provisionSnapshot] ERROR:', error);
    throw error;
  }
}

provisionSnapshot()
  .then(result => {
    console.log('\n✅ SUCCESS:', JSON.stringify(result, null, 2));
    process.exit(0);
  })
  .catch(error => {
    console.error('\n❌ FAILED:', error.message);
    process.exit(1);
  });
