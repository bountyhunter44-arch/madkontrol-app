"use strict";

const admin = require("firebase-admin");
const path  = require("path");

// ---------------------------------------------------------------------------
// Config
// ---------------------------------------------------------------------------

const SERVICE_ACCOUNT_PATH = path.resolve(__dirname, "../../serviceAccountKey.json");

const COMPANY_ID       = "company_1";
const LOCATION_ID      = "location_1";
const CREATED_BY       = "user_michael";
const CREATED_BY_NAME  = "Michael Nielsen";
const CREATED_BY_EMAIL = "michael@madkontrol.dk";

// ---------------------------------------------------------------------------
// Init
// ---------------------------------------------------------------------------

const serviceAccount = require(SERVICE_ACCOUNT_PATH);

admin.initializeApp({
    credential: admin.credential.cert(serviceAccount),
});

const db = admin.firestore();

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function log(msg) {
    process.stdout.write(`[seed] ${msg}\n`);
}

// ---------------------------------------------------------------------------
// Seed documents
// ---------------------------------------------------------------------------

/**
 * The user document is the only thing rutiner.html/auth.js needs at startup.
 * Everything else (task_templates, task_instances, verification_templates …)
 * is generated by the new egenkontrol pipeline on first page load.
 */
function buildUserDoc() {
    return {
        uid:               CREATED_BY,
        email:             CREATED_BY_EMAIL,
        displayName:       CREATED_BY_NAME,
        role:              "admin",
        companyId:         COMPANY_ID,
        locationId:        LOCATION_ID,
        locationIds:       [LOCATION_ID],
        primaryLocationId: LOCATION_ID,
        createdAt:         admin.firestore.FieldValue.serverTimestamp(),
        seedSource:        "seed-firestore-from-scratch",
    };
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

async function seed() {
    const batch = db.batch();
    let count = 0;

    // ── users/user_michael ──────────────────────────────────────────────────
    const userRef = db.collection("users").doc(CREATED_BY);
    const userSnap = await userRef.get();

    if (userSnap.exists) {
        log(`SKIP   users/${CREATED_BY} (already exists)`);
    } else {
        batch.set(userRef, buildUserDoc());
        log(`CREATE users/${CREATED_BY}`);
        count++;
    }

    // ── Commit ──────────────────────────────────────────────────────────────
    if (count === 0) {
        log("Nothing to write — Firestore is already seeded.");
        return;
    }

    await batch.commit();
    log(`Done. ${count} document(s) written.`);
    log("");
    log("Next step: open rutiner.html in the browser.");
    log("The egenkontrol pipeline will generate task_templates and task_instances automatically.");
}

seed().catch((err) => {
    process.stderr.write(`[seed] ERROR: ${err.message}\n`);
    process.exit(1);
});
